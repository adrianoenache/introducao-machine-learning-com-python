
"""
Exemplo 3: Listas em Python

Este exemplo mostra como criar e manipular listas.
Execute com: python 03_listas.py
"""


# Criação de uma lista de números
lista_numeros = [1, 2, 3, 4, 5]

# Soma o primeiro e o quarto elemento da lista (índices 0 e 3)
soma = lista_numeros[0] + lista_numeros[3]
print(f'Soma dos elementos de índice 0 e 3: {lista_numeros[0]} + {lista_numeros[3]} = {soma}')

# Criação de uma lista de cores
lista_cores = ['Vermelho', 'Amarelo', 'Azul', 'Verde', 'Roxo']

# Exibe a cor de índice 3 e a cor de índice 2
cor_3 = lista_cores[3]
cor_2 = lista_cores[2]
print(f'Cores de índice 3 e 2: {cor_3} e {cor_2}')
