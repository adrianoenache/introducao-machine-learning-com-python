
"""
Exemplo 1: Operações Básicas no Python

Este exemplo mostra como realizar operações matemáticas simples.
Execute com: python 01_operacoes_basicas.py
"""


# Operações matemáticas básicas

# Soma
resultado_soma = 5 + 10
print(f'Adição: 5 + 10 = {resultado_soma}')

# Subtração
resultado_subtracao = 20 - 6
print(f'Subtração: 20 - 6 = {resultado_subtracao}')

# Multiplicação
resultado_multiplicacao = 30 * 3
print(f'Multiplicação: 30 * 3 = {resultado_multiplicacao}')

# Divisão
resultado_divisao = 200 / 10
print(f'Divisão: 200 / 10 = {resultado_divisao}')

# Exponenciação
resultado_exponenciacao = 5 ** 3
print(f'Exponenciação: 5^3 = {resultado_exponenciacao}')
